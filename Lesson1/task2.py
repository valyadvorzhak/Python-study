#получить доступ к диапазону символов
#конструкция в виде: строка[номер начального символа:номер #конечного символа]

text = str(input('Введите текст: '))
print ('Количество символов в введенном тексте: ',(len(text)), 'символов')
s = int(input('Какой символ нужно начальный?: ')) - 1
f = int(input('Какой символ конечный?: ')) - 1
print (text[s:f])

